# Fallback glyph: a small rectangle (unknown character)
_UNKNOWN_GLYPH: list[int] = [
    0b01111110,
    0b01000010,
    0b01000010,
    0b01000010,
    0b01000010,
    0b01000010,
    0b01000010,
    0b01111110,
]

# Native glyph size
GLYPH_W = 8
GLYPH_H = 8


def _get_glyph(char: str) -> list[int]:
    """Return the 8-row bitmap for *char* (upper-cases if needed)."""
    if char in _GLYPHS:
        return _GLYPHS[char]
    upper = char.upper()
    if upper in _GLYPHS:
        return _GLYPHS[upper]
    return _UNKNOWN_GLYPH


def render_text(
    surface: pygame.Surface,
    text: str,
    x: int,
    y: int,
    color: tuple[int, int, int],
    scale: int = 1,
) -> int:
    """Render *text* onto *surface* at (x, y) using the pixel font.

    Parameters
    ----------
    surface:
        Target pygame Surface.
    text:
        String to render (uppercase recommended; lowercase is auto-upcased).
    x, y:
        Top-left origin in surface coordinates.
    color:
        RGB tuple for the 'lit' pixels.
    scale:
        Integer pixel scale factor (1 = 8px tall, 2 = 16px tall, etc.).

    Returns
    -------
    int
        X coordinate immediately after the last rendered character.
    """
    cx = x
    for char in text:
        glyph = _get_glyph(char)
        for row_idx, row_bits in enumerate(glyph):
            for col_idx in range(GLYPH_W):
                if row_bits & (0x80 >> col_idx):
                    px = cx + col_idx * scale
                    py = y + row_idx * scale
                    if scale == 1:
                        surface.set_at((px, py), color)
                    else:
                        pygame.draw.rect(surface, color, (px, py, scale, scale))
        cx += (GLYPH_W + 1) * scale  # 1-pixel gap between chars
    return cx


def text_width(text: str, scale: int = 1) -> int:
    """Return the pixel width of *text* at the given *scale*."""
    return len(text) * (GLYPH_W + 1) * scale


def text_height(scale: int = 1) -> int:
    """Return the pixel height of a single line at the given *scale*."""
    return GLYPH_H * scale
